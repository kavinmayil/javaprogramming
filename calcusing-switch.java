package onlinvedio;

public class Demo {

	public static void main(String[] args) {
	int n=5;
	int a,b,c;
	a=44;
	b=55;
	switch (n) {
	case 1:
		c=a+b;
		System.out.println(c);
		break;
	case 2: 
		c=a-b;
		System.out.println(c);
		break;
	case 3:
		c=a/b;
		System.out.println(c);
		break;
	case 4:
		c=a%b;
		System.out.println(c); 
	default:
		System.out.println("not found");
}
}
}
